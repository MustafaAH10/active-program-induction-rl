from __future__ import annotations

import argparse
import json
from pathlib import Path
from statistics import mean

from active_program_induction.dataset import read_jsonl
from active_program_induction.env import ActiveInductionEnv, initial_messages


def main() -> None:
    parser = argparse.ArgumentParser(description="Run true multi-turn cold-start evaluation.")
    parser.add_argument("--model", required=True, help="Hugging Face model id or local path")
    parser.add_argument("--tasks", required=True, help="task JSONL generated by api-bench")
    parser.add_argument("--out", default="data/generated/cold_start_multiturn_results.jsonl")
    parser.add_argument("--limit", type=int, default=50)
    parser.add_argument("--max-new-tokens", type=int, default=512)
    parser.add_argument("--temperature", type=float, default=0.7)
    parser.add_argument("--top-p", type=float, default=0.95)
    parser.add_argument("--dtype", default="auto")
    args = parser.parse_args()

    try:
        import torch
        from transformers import AutoModelForCausalLM, AutoTokenizer
    except ImportError as exc:
        raise SystemExit("Install GPU extras first: python3 -m pip install -e '.[gpu]'") from exc

    tokenizer = AutoTokenizer.from_pretrained(args.model, trust_remote_code=True)
    model = AutoModelForCausalLM.from_pretrained(
        args.model,
        torch_dtype=args.dtype,
        device_map="auto",
        trust_remote_code=True,
    )
    tasks = read_jsonl(args.tasks)[: args.limit]
    Path(args.out).parent.mkdir(parents=True, exist_ok=True)

    rewards = []
    correct = 0
    with Path(args.out).open("w", encoding="utf-8") as f:
        for task in tasks:
            env = ActiveInductionEnv(task, reward_mode="graded")
            messages = initial_messages(task)
            env.reset()
            turns = []
            final = None
            for _ in range(task["public"]["probe_budget"] + 2):
                prompt = tokenizer.apply_chat_template(
                    messages,
                    add_generation_prompt=True,
                    tokenize=False,
                )
                inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
                with torch.no_grad():
                    output = model.generate(
                        **inputs,
                        do_sample=args.temperature > 0,
                        temperature=args.temperature,
                        top_p=args.top_p,
                        max_new_tokens=args.max_new_tokens,
                        pad_token_id=tokenizer.eos_token_id,
                    )
                action_text = tokenizer.decode(
                    output[0][inputs["input_ids"].shape[-1] :],
                    skip_special_tokens=True,
                )
                step = env.step_text(action_text)
                turns.append({"assistant": action_text, "environment": step.observation})
                messages.append({"role": "assistant", "content": action_text})
                messages.append({"role": "user", "content": step.observation})
                if step.done:
                    final = step.info
                    break
            if final is None:
                final = {"equivalent": False, "reward": -0.1, "error": "no_submit"}
            rewards.append(float(final["reward"]))
            correct += int(bool(final.get("equivalent", False)))
            row = {
                "task_id": task["task_id"],
                "family": task["family"],
                "tier": task["tier"],
                "reward": final["reward"],
                "equivalent": final.get("equivalent", False),
                "num_queries": len(env.queries),
                "turns": turns,
                "score_detail": final,
            }
            f.write(json.dumps(row, sort_keys=True) + "\n")
            print(json.dumps({k: row[k] for k in ("task_id", "reward", "equivalent", "num_queries")}))

    summary = {
        "n": len(rewards),
        "mean_reward": mean(rewards) if rewards else 0.0,
        "min_reward": min(rewards) if rewards else 0.0,
        "max_reward": max(rewards) if rewards else 0.0,
        "pass_rate": correct / len(rewards) if rewards else 0.0,
        "out": args.out,
    }
    print(json.dumps(summary, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
